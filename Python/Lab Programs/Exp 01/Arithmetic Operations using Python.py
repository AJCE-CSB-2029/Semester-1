a = int(input("Enter first number: "))
b = int(input("Enter second number: "))

sum = a + b
diff = a - b
prod = a * b
div = a / b
mod = a % b
floor_div = a // b
exp = a ** b

print("Sum =", sum)
print("Difference =", diff)
print("Product =", prod)
print("Division =", div)
print("Modulus =", mod)
print("Floor Division =", floor_div)
print("Exponent =", exp)